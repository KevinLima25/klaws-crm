const {DatabaseSync}=require('node:sqlite');
const db=new DatabaseSync('n8n/data/database.sqlite');
const wf=db.prepare("SELECT * FROM workflow_entity WHERE id='WFCRM001chat01'").get();
const nodes=JSON.parse(wf.nodes);
const sn=nodes.find(n=>n.type==='n8n-nodes-base.switch');
console.log('Switch name:', sn.name);
console.log('Switch params:', JSON.stringify(sn.parameters, null, 2));
const conn=JSON.parse(wf.connections);
console.log('Connections:', JSON.stringify(conn, null, 2));
db.close();
