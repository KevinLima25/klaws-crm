const { DatabaseSync } = require('node:sqlite');
const db = new DatabaseSync('n8n/data/database.sqlite');
const wf = db.prepare("SELECT id, name, active, nodes, connections FROM workflow_entity WHERE id='WFCRM001chat01'").get();
const nodes = typeof wf.nodes === 'string' ? JSON.parse(wf.nodes) : wf.nodes;
const conns = typeof wf.connections === 'string' ? JSON.parse(wf.connections) : wf.connections;
console.log('Workflow:', wf.name, '| Active:', wf.active);
console.log('\n--- NODES ---');
nodes.forEach(n => console.log(`  ${n.name} (${n.type}) [id:${n.id}]`));
console.log('\n--- CONNECTIONS ---');
console.log(JSON.stringify(conns, null, 2));
db.close();
